# operators are used to perform operations on variables and values
# arithmetic operators
# example are: +, -, *, %
num_1 = 10
num_2 = 4
print(num_1+num_2)
print(num_1-num_2)
print(num_1*num_2)
print(num_1/num_2)
print(num_1%num_2)
# assignment operators
# example are: =, +=, -=, *= and /=
number_1 = 20
print(number_1)
number_1 += 5
print(number_1)
number_1 -= 2
print(number_1)
number_1 *= 2
print(number_1)
# comparison operators
# example are: ==, >,<,>=,<=
username = "adigun"
surname = "adigun"
print(username == surname)
size = 6
level = 30
print(size > level)
print(size < number_1)
print(level >= size)
# logical operators
# example are: not,!, or (||) , and (&&)
is_girl = False
print(not is_girl)
print(level < size or username == "adigun")
print(level < size and username == "adigun")
